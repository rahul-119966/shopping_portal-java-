    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = t1.getText();
        String address = t2.getText();
        String pin = t3.getText();
        String phone = t4.getText();
        boolean r = r1.isSelected();
        
        if(name!=null && address!=null && pin!=null && r==true && phone!=null)
        {
            try
            {
               BufferedWriter fw = new BufferedWriter(new FileWriter("shipping_address.txt",true));
                fw.append(name+" \n"+address+" \n"+phone+" \n"+pin+" \n\n ");
                fw.close();
                            
            }
            catch(Exception e)
            {
                System.out.println(e);
            }
            
            
            
        int n = JOptionPane.showConfirmDialog(null, "Are you sure?", "Message", JOptionPane.YES_NO_OPTION);
        if(n==JOptionPane.YES_OPTION)
        {
            JOptionPane.showMessageDialog(null, "Order Placed Successfully!");
            win w = new win();
            w.setVisible(true);
            this.dispose();
        }
       }
        else
        {
            JOptionPane.showMessageDialog(null, "Please enter all details");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

